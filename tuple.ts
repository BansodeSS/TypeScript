/**
 * tuple.ts
 * TypeScript Tuple Tutorial
 */

// Basic tuple declaration
let user: [string, number] = ["Suhas", 30];

// Accessing tuple elements
let tupleUserName: string = user[0];
let userAge: number = user[1];

// Usage
console.log("user:", user);
console.log("userName:", tupleUserName);
console.log("userAge:", userAge);